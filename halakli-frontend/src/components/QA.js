/** אייטם FAQ אחד – פתיחה באנימציה בסגנון "הוראות שימוש" */
function FaqItem({ q, a, defaultOpen = false }) {
  const [open, setOpen] = useState(defaultOpen);
  const panelRef = useRef(null);
  const bodyRef  = useRef(null);

  // אנימציית גובה כמו ב"הוראות שימוש"
  useEffect(() => {
    const panel = panelRef.current;
    const body  = bodyRef.current;
    if (!panel || !body) return;

    if (open) {
      const h = body.scrollHeight;
      panel.style.maxHeight = `${h}px`;
      panel.style.padding = "10px 0";
    } else {
      panel.style.maxHeight = "0";
      panel.style.padding = "0";
    }
  }, [open]);

  useEffect(() => {
    // מנקה וריאנט קיים של ההדר (כמו שהיה אצלך)
    document.body.classList.remove("header-variant-shop");

    // מתחילים מהחלק העליון של העמוד בכל כניסה ל-Q&A
    requestAnimationFrame(() => {
      window.scrollTo({ top: 0, left: 0, behavior: "auto" }); // אפשר "smooth" אם תרצי
    });
  }, []);

  return (
    <li className="faq-item" style={{ borderTop: 0 }}>
      <div className="pp-acc1" dir="rtl">
        <details className="pp-acc-usage" open={open}>
          <summary
            role="button"
            aria-expanded={open}
            onClick={(e) => {
              e.preventDefault();
              setOpen(v => !v);
            }}
            onKeyDown={(e) => {
              if (e.key === "Enter" || e.key === " ") {
                e.preventDefault();
                setOpen(v => !v);
              }
            }}
          >
            <span>{q}</span>
            <svg className="acc-icon" viewBox="0 0 24 24" aria-hidden="true">
              <path d="M6 9l6 6 6-6" fill="none" stroke="currentColor" strokeWidth="2" strokeLinecap="round" />
            </svg>
          </summary>

          <div className="acc-panel acc-js" ref={panelRef} style={{ maxHeight: 0, padding: 0, overflow: "hidden" }}>
            <div className="acc-body" ref={bodyRef}>
              <p style={{ margin: 0 }}>{a}</p>
            </div>
          </div>
        </details>
      </div>
    </li>
  );
}

